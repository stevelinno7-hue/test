        // 3. 通用註冊函式 (含年級隔離邏輯)
        // ==========================================
        function registerSubject(subjectName, subjectKeywords, db) {
            const grades = ["國八", "國九", "高一", "高二", "高三"]; // 化學/地科主要年級

            grades.forEach(grade => {
                // 1. 物理隔離：只篩選該年級
                const gradePool = db.filter(q => q.t[0] === grade);

                if (gradePool.length > 0) {
                    // 2. 註冊專屬模板 (如 chemistry_core_國八)
                    G.registerTemplate(`${subjectName}_core_${grade}`, (ctx, rnd) => {
                        const item = pick(gradePool);
                        
                        // 誘答：優先找同單元 (t[1]) 的錯誤答案
                        const sameUnit = gradePool.filter(x => x.t[1] === item.t[1] && x.a !== item.a);
                        const others = gradePool.filter(x => x.a !== item.a);
                        
                        let wrongOpts = [];
                        if (sameUnit.length >= 3) wrongOpts = shuffle(sameUnit).slice(0, 3).map(x => x.a);
                        else wrongOpts = shuffle(others).slice(0, 3).map(x => x.a);
                        
                        // 防呆
                        while(wrongOpts.length < 3) wrongOpts.push("其他");

                        const opts = shuffle([item.a, ...wrongOpts]);

                        return {
                            question: `【${subjectName === 'chemistry' ? '化學' : '地科'}】${item.q}，是指下列何者？`,
                            options: opts,
                            answer: opts.indexOf(item.a),
                            concept: item.t[1],
                            explanation: [`正確答案：${item.a}`]
                        };
                    }, [...subjectKeywords, grade]); // 標籤：科目 + 年級
                }
            });
        }

